package 循环结构;

public class TestFor {
    public static void main(String[] args) {


        for(int i=1;i<=10;i++){

            //循环体
            //System.out.print(1+" ");//输出10个1
            System.out.print(i+" ");
        }

        System.out.println();//打印空行

        for(int i=10;i>=1;i--){
            System.out.print(i+"  ");
        }
    }
}

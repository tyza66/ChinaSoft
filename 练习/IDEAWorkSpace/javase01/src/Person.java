public class Person {
    public static void main(String[] args) {
        String name = "小明";
        int age = 22;
        char sex = '男';
        double weight = 66;
        System.out.println("姓名:\t"+name);
        System.out.println("年龄:\t"+age);
        System.out.println("性别:\t"+sex);
        System.out.println("体重:\t"+weight);
        // \t是制表符 \n是换行符
    }
}
